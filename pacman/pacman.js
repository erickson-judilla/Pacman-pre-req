function restartPacman() {
	pacman.x = 1;
	pacman.y = 1;
}
function losePoints(obj) {
	for (var group in obj) {
		if (obj[group]["x"] == pacman.x && obj[group]["y"] == pacman.y) {
			life--;
			restartPacman();
		}
	}
	if (life == 0) {
		document.getElementById("endDisplay").style.display = "block";
		document.getElementById("endDisplay").innerHTML = "Game Over";
	}
}
